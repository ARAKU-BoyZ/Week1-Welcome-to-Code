// Code disini
let a = 1
while (a <= 5) {
  console.log(`LOOPING PERTAMA: ${a}`)
  a ++
}

console.log("\n")

let b = 5
while (b >= 1) {
  console.log(`LOOPING KEDUA: ${b}`)
  b --
}